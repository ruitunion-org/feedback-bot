using RuItUnion.FeedbackBot.SpamFilters;

namespace RuItUnion.FeedbackBot.Tests;

public class UaPropagandaFilterTests
{
    private readonly UaPropagandaFilter _filter = new();

    [Fact]
    public void IsSpam_NullOrEmpty_ReturnsFalse()
    {
        Assert.False(_filter.IsSpam(null!));
        Assert.False(_filter.IsSpam(string.Empty));
    }

    [Fact]
    public void IsSpam_NoMatches_ReturnsFalse()
    {
        Assert.False(_filter.IsSpam("Привет, мир!"));
        Assert.False(_filter.IsSpam("你好世界！"));
    }

    [Fact]
    public void IsSpam_SingleMatch_ReturnsFalse()
    {
        // Только одно совпадение ExchangeRegex
        Assert.False(_filter.IsSpam("обмен телами"));
    }

    [Fact]
    public void IsSpam_TwoMatchesSamePattern_ReturnsTrue()
    {
        // Два совпадения ServicemanRegex
        Assert.True(_filter.IsSpam("военнослужащие и военнослужащих"));
    }

    [Theory]
    [MemberData(nameof(RelevantUaPropagandaMessages))]
    public void IsSpam_RelevantMessages_ReturnsTrue(string message)
    {
        Assert.True(_filter.IsSpam(message));
    }

